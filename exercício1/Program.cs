int a = 17;
int b = 25;
int c = 32;

Console.WriteLine(a);
Console.WriteLine(b);
Console.WriteLine(c);

Console.WriteLine(a < b);
Console.WriteLine(b == c);

Console.WriteLine(++a);
Console.WriteLine(--c);
